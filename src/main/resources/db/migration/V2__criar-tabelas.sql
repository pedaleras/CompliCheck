CREATE TABLE TBL_CC_EMPRESA (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR2(100) NOT NULL,
    cnpj VARCHAR2(14) UNIQUE NOT NULL,
    setor VARCHAR2(50)
);

CREATE TABLE TBL_CC_NORMA (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    descricao VARCHAR2(255) NOT NULL,
    categoria VARCHAR2(50) NOT NULL,
    data_limite DATE NOT NULL,
    empresa_id NUMBER NOT NULL,
    FOREIGN KEY (empresa_id) REFERENCES TBL_CC_EMPRESA(id)
);

CREATE TABLE TBL_CC_ALERTA (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    status VARCHAR2(20) NOT NULL,
    data_verificacao DATE NOT NULL,
    norma_id NUMBER NOT NULL,
    FOREIGN KEY (norma_id) REFERENCES TBL_CC_NORMA(id)
);

CREATE TABLE TBL_CC_USUARIO (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR2(100) NOT NULL,
    email VARCHAR2(100) UNIQUE NOT NULL,
    senha VARCHAR2(100) NOT NULL,
    role VARCHAR2(50) NOT NULL,
    empresa_id NUMBER,
    FOREIGN KEY (empresa_id) REFERENCES TBL_CC_EMPRESA(id)
);
